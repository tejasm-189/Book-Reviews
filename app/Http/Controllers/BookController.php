     * Display a listing of the resource.
     */
    public function index()
    {
        $books = \App\Models\Book::when(request('title'), function ($query, $title) {
            return $query->title($title);
        })
            ->withCount('reviews')
            ->withAvg('reviews', 'rating')
            ->latest()
            ->get();

        return view('books.index', ['books' => $books]);
